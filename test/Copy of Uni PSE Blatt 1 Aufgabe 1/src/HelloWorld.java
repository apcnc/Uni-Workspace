
public class HelloWorld {
	public static void main(String[] args){
		String helloWorld = "Hallo Welt!";
		String javaIsAnIsland = "Java ist auch eine Insel";
		
		System.out.println("Hallo Welt!" );
		System.out.println("Java ist \n auch eine Insel");
		System.out.println(helloWorld);
		System.out.println(javaIsAnIsland);
		
		/*	System.out.println() macht einen Zeilenumbrach nach der Eingabe
		 *  System.out.print() macht keinen
		 *  
		 *  Zeilenumbrüche in String mit \n 
		 *  
		 *  Es ist sinnvoll variablen für alle Werte zu verwenden die nicht fest sind
		 */
	}
	
}


